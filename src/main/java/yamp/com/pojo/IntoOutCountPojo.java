package yamp.com.pojo;

public class IntoOutCountPojo {
	private double intoCount;
	private double outCount;
	private double SumCount;
	private double intoCountPercent;
	private double outCountPercent;

	
	public double getIntoCount() {
		return intoCount;
	}

	public void setIntoCount(double intoCount) {
		this.intoCount = intoCount;
	}

	public double getOutCount() {
		return outCount;
	}

	public void setOutCount(double outCount) {
		this.outCount = outCount;
	}

	public double getSumCount() {
		return SumCount;
	}

	public void setSumCount(double sumCount) {
		SumCount = sumCount;
	}

	public double getIntoCountPercent() {
		return intoCountPercent;
	}

	public void setIntoCountPercent(double intoCountPercent) {
		this.intoCountPercent = intoCountPercent;
	}

	public double getOutCountPercent() {
		return outCountPercent;
	}

	public void setOutCountPercent(double outCountPercent) {
		this.outCountPercent = outCountPercent;
	}

}
